    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cityName = cityText.getText().trim();
        if(cityName.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "please enter a city name.");
            return;
        }
        try{
            String apiKey = "1837bb281a81fc49b259309909ef52f8";
            String urlString = "http://api.openweathermap.org/data/2.5/weather?q="+cityName+"&appid="+apiKey+"&units=metric";
            
            java.net.URL url = new java.net.URL(urlString);
            java.net.HttpURLConnection conn = (java.net.HttpURLConnection) url.openConnection();
            conn.setRequestMethod("GET");
            
            java.io.BufferedReader reader = new java.io.BufferedReader(new java.io.InputStreamReader(conn.getInputStream()));
            String line;
            StringBuilder response = new StringBuilder();
            
            while((line=reader.readLine())!=null){
                response.append(line);
            }
            reader.close();
            
            JSONObject jsonResponse = new JSONObject(response.toString());
            JSONObject main = jsonResponse.getJSONObject("main");
            JSONObject wind = jsonResponse.getJSONObject("wind");
            
            double temperature = main.optDouble("temp");
            int humidityvalue = main.optInt("humidity");
            double windSpeed = wind.optDouble("speed");
            
            tempText.setText(temperature+"Â°C");
            humidityText.setText(humidityvalue+"%");
            windText.setText(windSpeed+"m/s");
            
//            temp.repaint();
//            humidity.repaint();
//            windsp.repaint();
//            this.getContentPane().revalidate();  // Refresh the entire frame
//            this.getContentPane().repaint();
            
            
            System.out.println(response.toString());
            System.out.println(temperature);
            System.out.println(humidityvalue);
        }
        catch(Exception e){
            javax.swing.JOptionPane.showMessageDialog(this, "Error fetching weather data.");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

